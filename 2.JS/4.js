/*27. Informar se um número inteiro está ou não dentro de um intervalo de 2 número digitado*/

let num = 15;
let intervalo1 = 10;
let intervalo2 = 20;

if (num >= intervalo1 && num <= intervalo2) {
    console.log(num + " está dentro do intervalo");
} else {
    console.log(num + " não está no intervalo");
}

